public class ex18 {
    public static void main(String[] args) {
        // definir o número que você deseja verificar
        double numero = -5.0; // alterar este valor para o número desejado
        
        // verificar se o número é positivo, negativo ou zero e imprima o resultado
        if (numero > 0) {
            System.out.println(numero + " é um número positivo.");
        } else if (numero < 0) {
            System.out.println(numero + " é um número negativo.");
        } else {
            System.out.println(numero + " é igual a zero.");
        }
    }
}
